/*
    Built-In Middleware Concepts
    ============================
    Starting with version 4.x, Express no longer depends on 'Connect'.
    The middleware functions that were previously included with
    Express are now in separate modules.

    Express has the following built-in middleware functions:

    a. express.static       --> serves static assets such as HTML files, images, and so on.
    b. express.json         --> parses incoming requests with JSON payloads.
                                NOTE: Available with Express 4.16.0+
    c. express.urlencoded   --> parses incoming requests with URL-encoded payloads.
                                NOTE: Available with Express 4.16.0+

*/
